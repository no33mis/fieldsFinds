//create jQuery click and show functions for each rectangle with unique ID
$(window).load(function () {
	// assign the function by click onto class and show the ID
	$(".infoBox1").click(function(){
	   $('#field1').show();
	});
	 // show the grey background when clicking on class
	$('.hoverInfo').click(function(){
		$('#field1').hide();
	});
	// remove the modal when clicking on the closing button
	$('#fieldbutton1').click(function(){
		$('#field1').hide();
	});
});

